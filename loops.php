<?php
#####  While Loop #####
$a = 1;
$output = '';

while($a<5){
    $output .= $output . '<BR>This is a'. $a;
    $a++;

}

#### Do While Loop ####
do{
    $b = 1;
    $output = '';
    $output .= $output . '<BR>This is do while'. $a;
    $b++;

} while(b<5);

#### For Loop ####
for(a =0;a<7; a++){
    $output .= '<BR>This is for loop '. $a;

}
?>
